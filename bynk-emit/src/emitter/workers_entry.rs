/// Generate the per-route dispatch block for one `on http` handler. The
/// router has already been entered via `try`; this block extracts path
/// parameters, deserialises the body (when present), invokes the handler,
/// and serialises the HttpResult through `httpResultToResponse`.
fn emit_http_route_dispatch(
    out: &mut String,
    route: &HttpRoute,
    cors_const: Option<&str>,
    security_const: Option<&str>,
) {
    let h = &route.handler;
    let method_key = http_handler_method_name(route.method, &route.path);
    let has_path_params = param_count(&route.path) > 0;
    let path_lit = route.path.replace('"', "\\\"");
    // v0.139 (ADR 0162 D3): a `GET` route also answers `HEAD` — the guard widens
    // so the `GET` handler runs, then the built response's body is stripped
    // below. Other methods match exactly.
    let method_guard = if route.method == HttpMethod::Get {
        "(method === \"GET\" || method === \"HEAD\")".to_string()
    } else {
        format!("method === \"{}\"", route.method.as_str())
    };
    let _ = writeln!(out, "      {{");
    if has_path_params {
        let _ = writeln!(out, "        const __m = matchPath(\"{path_lit}\", path);");
        let _ = writeln!(out, "        if ({method_guard} && __m) {{");
    } else {
        let _ = writeln!(
            out,
            "        if ({method_guard} && path === \"{path_lit}\") {{"
        );
    }
    // v0.142 (ADR 0165): the request-body ceiling. A route with an effective cap
    // rejects an oversized body with a synthesised `413` derived from the declared
    // `Content-Length`, *before* the body is read — ahead of the `by`/Bearer/sum
    // wrapper and the Signature seam's raw read, so an oversized (possibly
    // unauthenticated) body is never buffered (DECISION D/E). The `413` is
    // `applyCors`/`applySecurityHeaders`-stamped so a cross-origin caller can read
    // it, the same visibility rule the `405` follows. Emitted only for a
    // body-taking route with a cap; a capless route is byte-for-byte unchanged
    // (opt-in). This placement also covers a multi-actor sum route, whose wrapper
    // reads the body itself.
    let has_body = h.params.iter().any(|p| p.name.name == "body");
    if let Some(cap) = route.max_body.filter(|_| has_body) {
        let inner = format!(
            "new Response(JSON.stringify({{ kind: \"PayloadTooLarge\", details: \"request body exceeds {cap} bytes\" }}), {{ status: 413, headers: {{ \"content-type\": \"application/json\" }} }})"
        );
        let corsed = match cors_const {
            Some(c) => format!("applyCors({inner}, {c}, request.headers.get(\"origin\"))"),
            None => inner,
        };
        let stamped = match security_const {
            Some(s) => format!("applySecurityHeaders({corsed}, {s})"),
            None => corsed,
        };
        let _ = writeln!(
            out,
            "          const __contentLength = request.headers.get(\"content-length\");"
        );
        let _ = writeln!(
            out,
            "          if (__contentLength !== null && Number(__contentLength) > {cap}) {{"
        );
        let _ = writeln!(out, "            return {stamped};");
        let _ = writeln!(out, "          }}");
    }
    // Extract path parameters from the matched params map.
    let mut call_args: Vec<String> = Vec::new();
    for p in &h.params {
        let pname = &p.name.name;
        if pname == "body" {
            // Body deserialisation happens below.
            continue;
        }
        // It's a path parameter — extract from __m.params and construct.
        let _ = writeln!(
            out,
            "          const __raw_{pname} = __m.params[\"{pname}\"];"
        );
        emit_path_param_construction(out, pname, &p.type_ref);
        call_args.push(pname.clone());
    }
    // Body parameter (POST/PUT/PATCH). A multi-actor sum route's wrapper reads
    // and parses the body itself (it must verify over the raw bytes first), so
    // the entry skips the body here and passes only `request`.
    if !route.sum
        && let Some(body_param) = h.params.iter().find(|p| p.name.name == "body")
    {
        let _ = writeln!(out, "          let __body_json: JsonValue;");
        if let Some(seam) = &route.signature {
            // v0.51: read the raw body once, verify the HMAC fail-closed (401),
            // then parse the body param from the *same* bytes (not a re-read /
            // re-serialisation — the signature is over these exact bytes).
            let secret = crate::emitter::escape_ts_string(&seam.secret);
            let header = crate::emitter::escape_ts_string(&seam.header);
            let _ = writeln!(out, "          let __raw: string;");
            let _ = writeln!(out, "          try {{");
            let _ = writeln!(out, "            __raw = await request.text();");
            let _ = writeln!(out, "          }} catch {{");
            let _ = writeln!(
                out,
                "            return new Response(JSON.stringify({{ kind: \"MalformedJson\", details: \"Invalid request body\" }}), {{ status: 400, headers: {{ \"content-type\": \"application/json\" }} }});"
            );
            let _ = writeln!(out, "          }}");
            let _ = writeln!(
                out,
                "          const __secret = (env as unknown as Record<string, unknown>)[\"{secret}\"] ?? (globalThis as {{ process?: {{ env?: Record<string, unknown> }} }}).process?.env?.[\"{secret}\"];"
            );
            let _ = writeln!(
                out,
                "          if (typeof __secret !== \"string\") return new Response(null, {{ status: 401 }});"
            );
            // Timestamp (when bound): must be present; passed to the verifier.
            let ts_expr = match &seam.timestamp_header {
                Some(th) => {
                    let th = crate::emitter::escape_ts_string(th);
                    let _ = writeln!(out, "          const __ts = request.headers.get(\"{th}\");");
                    let _ = writeln!(
                        out,
                        "          if (__ts === null) return new Response(null, {{ status: 401 }});"
                    );
                    "__ts"
                }
                None => "null",
            };
            let tol = match seam.tolerance_secs {
                Some(n) => n.to_string(),
                None => "null".to_string(),
            };
            let _ = writeln!(
                out,
                "          const __ok = await verifySignatureHmacSha256(__raw, __secret, request.headers.get(\"{header}\"), {ts_expr}, {tol});"
            );
            let _ = writeln!(
                out,
                "          if (!__ok) return new Response(null, {{ status: 401 }});"
            );
            let _ = writeln!(out, "          try {{");
            let _ = writeln!(
                out,
                "            __body_json = JSON.parse(__raw) as JsonValue;"
            );
            let _ = writeln!(out, "          }} catch {{");
            let _ = writeln!(
                out,
                "            return new Response(JSON.stringify({{ kind: \"MalformedJson\", details: \"Invalid request body\" }}), {{ status: 400, headers: {{ \"content-type\": \"application/json\" }} }});"
            );
            let _ = writeln!(out, "          }}");
        } else {
            let _ = writeln!(out, "          try {{");
            let _ = writeln!(
                out,
                "            __body_json = (await request.json()) as JsonValue;"
            );
            let _ = writeln!(out, "          }} catch {{");
            let _ = writeln!(
                out,
                "            return new Response(JSON.stringify({{ kind: \"MalformedJson\", details: \"Invalid request body\" }}), {{ status: 400, headers: {{ \"content-type\": \"application/json\" }} }});"
            );
            let _ = writeln!(out, "          }}");
        }
        let dser = deserialise_call(&body_param.type_ref, "__body_json", "$");
        let _ = writeln!(out, "          const __r_body = {dser};");
        let _ = writeln!(
            out,
            "          if (__r_body.tag === \"Err\") return new Response(JSON.stringify(__r_body.error), {{ status: 400, headers: {{ \"content-type\": \"application/json\" }} }});"
        );
        let _ = writeln!(out, "          const body = __r_body.value;");
        call_args.push("body".to_string());
    }
    // Invoke the handler and serialise the HttpResult. The handler is
    // wrapped on the surface so its deps are wired by `compose`. v0.47: a
    // Bearer wrapper takes the request first (it runs the verification seam).
    let surface_args = if route.bearer || route.sum || route.oidc {
        // The Bearer, Oidc, and sum wrappers take the request first (they run the
        // verification seam); a sum wrapper also reads/parses the body itself,
        // so `call_args` here carries only the path params.
        let mut a = vec!["request".to_string()];
        a.extend(call_args.iter().cloned());
        a.join(", ")
    } else {
        call_args.join(", ")
    };
    let _ = writeln!(
        out,
        "          const result = await surface.{method_key}({surface_args});",
    );
    let _ = route.service.as_str();
    let inner = http_result_inner(&h.return_type);
    let ser_fn = http_value_serialiser(&inner);
    // v0.140 (ADR 0163): a `GET` response carries a weak `ETag` (an `Ok` body gets
    // the validator via `weakEtag`), an optional `@cache` `Cache-Control`
    // (`applyCache`), and is answered `304` when the request revalidates
    // (`notModifiedIfMatch`) — composed innermost-first so CORS still stamps the
    // `304`. Non-`GET`/unsafe methods are byte-for-byte unchanged: no validator, no
    // conditional, no freshness (DECISION B).
    let response_expr = if route.method == HttpMethod::Get {
        let base = format!("httpResultToResponse(result, {ser_fn}, {{ weakEtag: true }})");
        let cached = match &route.cache {
            Some(p) => format!("applyCache({base}, {}, \"{}\")", p.max_age_secs, p.scope),
            None => base,
        };
        format!("notModifiedIfMatch({cached}, request)")
    } else {
        format!("httpResultToResponse(result, {ser_fn})")
    };
    // v0.131: a CORS-enabled service stamps the `Access-Control-*` headers onto
    // every real response, uniformly across variant families — including the
    // synthesised `304`, so a cross-origin revalidation stays readable.
    let cors_expr = match cors_const {
        Some(c) => format!("applyCors({response_expr}, {c}, request.headers.get(\"origin\"))"),
        None => response_expr,
    };
    // v0.141 (ADR 0164): stamp the security headers (`nosniff` by default, HSTS
    // opt-in) onto every response — outside `applyCors`, but the two set disjoint
    // headers so order is immaterial. Every `from http` service has a policy, so
    // this is `Some` on every route; it also flows onto the `304` (inside
    // `response_expr`) and the `HEAD` answer (`headResponse` copies the headers).
    let build_expr = match security_const {
        Some(s) => format!("applySecurityHeaders({cors_expr}, {s})"),
        None => cors_expr,
    };
    // v0.139 (ADR 0162 D3): on a `GET` route, a `HEAD` returns the same status
    // and headers with an empty body — the handler ran, so the headers are the
    // real ones a `GET` would produce; `headResponse` discards the body without
    // reading it (a `Streaming` body is never drained).
    if route.method == HttpMethod::Get {
        let _ = writeln!(out, "          const __response = {build_expr};");
        let _ = writeln!(
            out,
            "          return method === \"HEAD\" ? headResponse(__response) : __response;"
        );
    } else {
        let _ = writeln!(out, "          return {build_expr};");
    }
    let _ = writeln!(out, "        }}");
    let _ = writeln!(out, "      }}");
}

/// Synthesise a deps object literal for invoking a handler from the
/// fetch entry point. Mirrors `compose`'s deps construction so handlers
/// see the same shape whether invoked through the surface or directly.
fn emit_call_handler_dispatch(
    out: &mut String,
    sname: &str,
    h: &Handler,
    actors: &std::collections::HashMap<String, bynk_syntax::ast::ActorDecl>,
    local_types: &std::collections::HashMap<String, TypeDecl>,
) {
    // v0.54: a `by c: Caller` handler reads the caller's context name from the
    // `X-Bynk-Caller` header (stamped by `callService`) and threads it into the
    // surface call. The internal channel is trusted, but a missing caller means
    // a malformed / non-Bynk call — fail-closed (the `Internal` 401-analogue).
    let caller_args = if bynk_check::actors::caller_binder_for(h, actors).is_some() {
        let _ = writeln!(
            out,
            "            const __caller = request.headers.get(\"X-Bynk-Caller\");"
        );
        let _ = writeln!(
            out,
            "            if (__caller === null || __caller === \"\") return new Response(JSON.stringify({{ kind: \"Unauthorized\", details: \"missing caller identity\" }}), {{ status: 401, headers: {{ \"content-type\": \"application/json\" }} }});"
        );
        "__caller, "
    } else {
        ""
    };
    if h.params.len() == 1 {
        let p = &h.params[0];
        let pname = &p.name.name;
        let dser_call = deserialise_call(&p.type_ref, "args", "$");
        let _ = writeln!(out, "            const __r_{pname} = {dser_call};");
        let _ = writeln!(
            out,
            "            if (__r_{pname}.tag === \"Err\") return new Response(JSON.stringify(__r_{pname}.error), {{ status: 400, headers: {{ \"content-type\": \"application/json\" }} }});"
        );
        let _ = writeln!(
            out,
            "            const {pname} = __r_{pname}.value{};",
            brand_assertion(&p.type_ref, local_types)
        );
        let _ = writeln!(
            out,
            "            const result = await surface.{sname}({caller_args}{pname});"
        );
    } else {
        let _ = writeln!(
            out,
            "            if (typeof args !== \"object\" || args === null || Array.isArray(args)) return new Response(JSON.stringify({{ kind: \"StructuralMismatch\", path: \"$\", expected: \"object\", actual: typeof args }}), {{ status: 400, headers: {{ \"content-type\": \"application/json\" }} }});"
        );
        let _ = writeln!(
            out,
            "            const argsObj = args as {{ [k: string]: JsonValue }};"
        );
        let mut names = Vec::new();
        for p in &h.params {
            let pname = &p.name.name;
            let dser = deserialise_call(
                &p.type_ref,
                &format!("argsObj[\"{pname}\"]"),
                &format!("$.{pname}"),
            );
            let _ = writeln!(out, "            const __r_{pname} = {dser};");
            let _ = writeln!(
                out,
                "            if (__r_{pname}.tag === \"Err\") return new Response(JSON.stringify(__r_{pname}.error), {{ status: 400, headers: {{ \"content-type\": \"application/json\" }} }});"
            );
            let _ = writeln!(
                out,
                "            const {pname} = __r_{pname}.value{};",
                brand_assertion(&p.type_ref, local_types)
            );
            names.push(pname.clone());
        }
        // Prepend the caller (when present) without a dangling comma for the
        // zero-param case.
        let mut call_args: Vec<&str> = Vec::new();
        if !caller_args.is_empty() {
            call_args.push("__caller");
        }
        call_args.extend(names.iter().map(String::as_str));
        let _ = writeln!(
            out,
            "            const result = await surface.{sname}({});",
            call_args.join(", ")
        );
    }
    let ser_expr = serialise_call(&h.return_type, "result");
    let _ = writeln!(out, "            const body = {ser_expr};");
    let _ = writeln!(
        out,
        "            return new Response(JSON.stringify(body), {{ status: 200, headers: {{ \"content-type\": \"application/json\" }} }});"
    );
}

/// Emit code that converts the raw path-parameter string to the parameter's
/// declared type. For plain `String`, this is a direct assignment; for
/// refined or opaque `String`-based types we lower through `T.of(...)`
/// which performs refinement validation and returns 400 on failure.
fn emit_path_param_construction(out: &mut String, pname: &str, t: &TypeRef) {
    match t {
        TypeRef::Base(BaseType::String, _) => {
            let _ = writeln!(out, "          const {pname} = __raw_{pname};");
        }
        TypeRef::Named(id) => {
            let _ = writeln!(
                out,
                "          const __r_{pname} = handlers.{}.of(__raw_{pname});",
                id.name
            );
            let _ = writeln!(
                out,
                "          if (__r_{pname}.tag === \"Err\") return new Response(JSON.stringify({{ kind: \"RefinementViolation\", path: \"path.{pname}\", violation: __r_{pname}.error }}), {{ status: 400, headers: {{ \"content-type\": \"application/json\" }} }});"
            );
            let _ = writeln!(out, "          const {pname} = __r_{pname}.value;");
        }
        _ => {
            // Other shapes are rejected by the static check; emit a fallback.
            let _ = writeln!(out, "          const {pname} = __raw_{pname} as any;");
        }
    }
}

/// Strip the `Effect[HttpResult[_]]` wrapper to expose the inner payload type
/// `T`. Used to choose the right serialiser when emitting the HttpResult
/// body.
fn http_result_inner(t: &TypeRef) -> TypeRef {
    let inner = match t {
        TypeRef::Effect(t, _) => t.as_ref(),
        other => other,
    };
    match inner {
        TypeRef::HttpResult(payload, _) => (**payload).clone(),
        other => other.clone(),
    }
}

/// TypeScript expression that serialises a value of the `HttpResult[T]`
/// payload type to a `JsonValue`. Wraps the per-type serialise helper for
/// named types; uses pass-through for base types.
fn http_value_serialiser(t: &TypeRef) -> String {
    match t {
        TypeRef::Base(_, _) => "(v: any) => v as JsonValue".to_string(),
        // v0.20a: function types are confined to non-boundary positions
        // (`bynk.types.function_at_boundary`), so the serialisation machinery
        // can never legally see one.
        TypeRef::Fn(..)
        | TypeRef::Query(..)
        | TypeRef::Stream(..)
        | TypeRef::Connection(..)
        | TypeRef::History(..) => {
            unreachable!("function/query/stream types are rejected at boundaries")
        }
        // v0.174 (#592): a generic-record instantiation delegates to its
        // monomorphised codec.
        TypeRef::App { .. } => {
            let inst_name = inner_ts_name(t);
            format!("handlers.serialise_{inst_name}")
        }
        TypeRef::Unit(_) => "(_v: any) => null".to_string(),
        TypeRef::Named(id) => format!("handlers.serialise_{}", id.name),
        TypeRef::Result(_, _, _)
        | TypeRef::Option(_, _)
        | TypeRef::List(_, _)
        | TypeRef::Map(_, _, _) => {
            let inst_name = inner_ts_name(t);
            format!("handlers.serialise_{inst_name}")
        }
        TypeRef::Effect(inner, _) => http_value_serialiser(inner),
        TypeRef::HttpResult(_, _)
        | TypeRef::QueueResult(_)
        | TypeRef::ValidationError(_)
        | TypeRef::JsonError(_) => "(v: any) => v as JsonValue".to_string(),
    }
}

/// v0.176 (#642): the callee side of the workers cross-context boundary. Both
/// of these used to be a *parallel* dispatch that shadowed
/// `serialisation.rs`'s — and drifted from it. `serialise_call` cast a `Bytes`
/// to `JsonValue` while `deserialise_call` base64-decoded it, the asymmetry that
/// made `bynk.types.bytes_at_workers_boundary` necessary. They are now one line
/// each over the shared dispatch, reaching this context's helpers through the
/// `handlers` namespace the entry point imports.
pub(crate) fn deserialise_call(t: &TypeRef, json_expr: &str, path: &str) -> String {
    crate::emitter::serialisation::deserialise_expr_via(t, json_expr, path, "handlers.")
}

fn serialise_call(t: &TypeRef, value: &str) -> String {
    crate::emitter::serialisation::serialise_expr_via(t, value, "handlers.")
}

fn inner_ts_name(t: &TypeRef) -> String {
    match t {
        TypeRef::Base(b, _) => b.name().to_string(),
        // v0.20a: function types are confined to non-boundary positions
        // (`bynk.types.function_at_boundary`), so the serialisation machinery
        // can never legally see one.
        TypeRef::Fn(..)
        | TypeRef::Query(..)
        | TypeRef::Stream(..)
        | TypeRef::Connection(..)
        | TypeRef::History(..) => {
            unreachable!("function/query/stream types are rejected at boundaries")
        }
        // v0.174 (#592): a generic-record instantiation names its monomorphised
        // codec — `Paginated[User]` → `Paginated_User`.
        TypeRef::App { name, args, .. } => {
            let mut s = name.name.clone();
            for a in args {
                s.push('_');
                s.push_str(&inner_ts_name(a));
            }
            s
        }
        TypeRef::Named(id) => id.name.clone(),
        TypeRef::Result(a, b, _) => format!("Result_{}_{}", inner_ts_name(a), inner_ts_name(b)),
        TypeRef::Option(a, _) => format!("Option_{}", inner_ts_name(a)),
        TypeRef::Effect(a, _) => format!("Effect_{}", inner_ts_name(a)),
        TypeRef::HttpResult(a, _) => format!("HttpResult_{}", inner_ts_name(a)),
        TypeRef::List(a, _) => format!("List_{}", inner_ts_name(a)),
        TypeRef::Map(k, v, _) => format!("Map_{}_{}", inner_ts_name(k), inner_ts_name(v)),
        TypeRef::QueueResult(_) => "QueueResult".to_string(),
        TypeRef::ValidationError(_) => "ValidationError".to_string(),
        TypeRef::JsonError(_) => "JsonError".to_string(),
        TypeRef::Unit(_) => "Unit".to_string(),
    }
}

/// v0.176 (#642): re-assert a deserialised value into this context's *branded*
/// view of a type it `uses` from a commons.
///
/// A context rebrands the commons types it `uses` (`Money & { __ctxBrand:
/// "commerce.orders" }`, ADR §6.2) so the same commons type is nominally distinct
/// per context. The boundary codec for such a type lives in the *commons* module
/// and necessarily returns the **unbranded** commons type, while the handler it
/// feeds is typed against the branded one — so the entry must bridge them.
/// Routing through `unknown` is the established spelling: a direct cast is
/// rejected under `tsc --strict` because the brand discriminants are
/// incompatible, and Bynk has already guaranteed the value's shape structurally
/// at the boundary (the same reasoning as the bundle path's cross-context
/// argument cast). The gap has always existed; it was invisible while the compose
/// wrapper typed every parameter `any` (Decision E).
///
/// **Only an imported type is asserted, and the narrowness is the point.** An
/// `as unknown as T` is exactly the unchecked assertion this increment exists to
/// delete, so it must not be applied one position wider than the gap it bridges.
/// For a **context-declared** type there is no brand gap at all: the type is
/// declared here, `handlers.ts` exports it, and `deserialise_<T>` already returns
/// precisely `handlers.<T>` — asserting there would re-disarm the very check
/// Decision E just bought, letting a wrong codec type-check again. `table.types`
/// holds only this unit's own declarations, so absence from it means the name was
/// imported (the `uses`-commons case, where the brands genuinely differ).
///
/// Only a *named root* is asserted, mirroring the bundle path: for a generic
/// (`List[Money]`) TypeScript resolves the brand through the intersection.
fn brand_assertion(
    t: &TypeRef,
    local_types: &std::collections::HashMap<String, TypeDecl>,
) -> String {
    match crate::emitter::emit::type_ref_named_root(t) {
        Some(name) if !local_types.contains_key(name) => {
            format!(" as unknown as handlers.{name}")
        }
        _ => String::new(),
    }
}

//! TypeScript emission (spec §7; v0.1 §6).
//!
//! Walks the typed AST and writes a single TypeScript module. Refined types
//! become a branded type alias plus a constructor object with `of` and
//! `unsafe`. Functions become `export function`s whose body is the lowering
//! of the Karn block.
//!
//! v0.1 lowering rules (see grammar §6):
//! - `let x = expr` becomes `const x = expr;`.
//! - `expr?` introduces a fresh temporary `__rN`, an `if (!__rN.ok) return __rN;`
//!   early-return, and replaces itself with `__rN.value`.
//! - `if cond { a } else { b }` becomes a ternary when both branches are
//!   simple, else an IIFE.
//! - `Ok(x)` / `Err(x)` lower to runtime calls of the same name.
//! - Constructor calls `TypeName.of(value)` use the constructor object's
//!   `of` method (already emitted by the v0 layer for refined types).

use std::fmt::Write as _;

use crate::ast::*;
use crate::checker::TypedCommons;

const RUNTIME_IMPORT: &str = "./runtime.js";
const INDENT_STEP: usize = 2;

/// Emit TypeScript source for the typed commons.
pub fn emit(commons: &TypedCommons) -> String {
    let mut out = String::new();
    write_header(&mut out, commons);

    for item in &commons.commons.items {
        match item {
            CommonsItem::Type(t) => emit_type(&mut out, t),
            CommonsItem::Fn(f) => emit_fn(&mut out, f),
        }
    }

    out
}

fn write_header(out: &mut String, commons: &TypedCommons) {
    writeln!(out, "// Generated by karnc — do not edit by hand.",).unwrap();
    writeln!(out, "// commons {}", commons.commons.name.joined()).unwrap();
    writeln!(out).unwrap();
    if needs_runtime_import(commons) {
        writeln!(
            out,
            "import {{ Ok, Err, type Result, type ValidationError }} from \"{RUNTIME_IMPORT}\";",
        )
        .unwrap();
        writeln!(out).unwrap();
    }
}

/// True if the emitted module references anything from the runtime —
/// any refined type (its `of` method emits `Ok`/`Err`/`Result`/`ValidationError`),
/// any function returning `Result`/`ValidationError`, or any expression
/// using `Ok`, `Err`, `?`, or a constructor call.
fn needs_runtime_import(commons: &TypedCommons) -> bool {
    for item in &commons.commons.items {
        match item {
            CommonsItem::Type(_) => return true,
            CommonsItem::Fn(f) => {
                if type_ref_uses_runtime(&f.return_type) {
                    return true;
                }
                for p in &f.params {
                    if type_ref_uses_runtime(&p.type_ref) {
                        return true;
                    }
                }
                if block_uses_runtime(&f.body) {
                    return true;
                }
            }
        }
    }
    false
}

fn type_ref_uses_runtime(r: &TypeRef) -> bool {
    matches!(r, TypeRef::Result(_, _, _) | TypeRef::ValidationError(_))
}

fn block_uses_runtime(b: &Block) -> bool {
    for s in &b.statements {
        match s {
            Statement::Let(l) => {
                if l.type_annot.as_ref().is_some_and(type_ref_uses_runtime) {
                    return true;
                }
                if expr_uses_runtime(&l.value) {
                    return true;
                }
            }
        }
    }
    expr_uses_runtime(&b.tail)
}

fn expr_uses_runtime(e: &Expr) -> bool {
    match &e.kind {
        ExprKind::Ok(_)
        | ExprKind::Err(_)
        | ExprKind::Question(_)
        | ExprKind::ConstructorCall { .. } => true,
        ExprKind::Paren(i) | ExprKind::UnaryOp(_, i) => expr_uses_runtime(i),
        ExprKind::BinOp(_, l, r) => expr_uses_runtime(l) || expr_uses_runtime(r),
        ExprKind::Call(_, args) => args.iter().any(expr_uses_runtime),
        ExprKind::Block(b) => block_uses_runtime(b),
        ExprKind::If {
            cond,
            then_block,
            else_block,
        } => {
            expr_uses_runtime(cond)
                || block_uses_runtime(then_block)
                || block_uses_runtime(else_block)
        }
        _ => false,
    }
}

fn emit_type(out: &mut String, t: &TypeDecl) {
    let ts_base = ts_base(t.base);
    writeln!(
        out,
        "export type {name} = {base} & {{ readonly __brand: \"{name}\" }};",
        name = t.name.name,
        base = ts_base,
    )
    .unwrap();
    writeln!(out).unwrap();
    writeln!(out, "export const {name} = {{", name = t.name.name).unwrap();
    writeln!(
        out,
        "  of(value: {base}): Result<{name}, ValidationError> {{",
        name = t.name.name,
        base = ts_base,
    )
    .unwrap();
    emit_checks(out, t);
    writeln!(out, "    return Ok(value as {name});", name = t.name.name).unwrap();
    writeln!(out, "  }},").unwrap();
    writeln!(
        out,
        "  unsafe(value: {base}): {name} {{",
        name = t.name.name,
        base = ts_base,
    )
    .unwrap();
    writeln!(out, "    return value as {name};", name = t.name.name).unwrap();
    writeln!(out, "  }},").unwrap();
    writeln!(out, "}};").unwrap();
    writeln!(out).unwrap();
}

fn emit_checks(out: &mut String, t: &TypeDecl) {
    let name = &t.name.name;

    // Shape check (Int requires integer input; String is enforced by the TS signature).
    if t.base == BaseType::Int {
        writeln!(out, "    if (!Number.isInteger(value)) {{").unwrap();
        writeln!(
            out,
            "      return Err({{ field: \"{name}\", message: \"must be an integer\", value }});",
        )
        .unwrap();
        writeln!(out, "    }}").unwrap();
    }

    let Some(refinement) = &t.refinement else {
        return;
    };
    for pred in &refinement.predicates {
        emit_pred_check(out, name, &pred.kind);
    }
}

fn emit_pred_check(out: &mut String, type_name: &str, pred: &PredKind) {
    match pred {
        PredKind::NonNegative => {
            writeln!(out, "    if (!(value >= 0)) {{").unwrap();
            writeln!(
                out,
                "      return Err({{ field: \"{type_name}\", message: \"must be non-negative\", value }});",
            )
            .unwrap();
            writeln!(out, "    }}").unwrap();
        }
        PredKind::Positive => {
            writeln!(out, "    if (!(value > 0)) {{").unwrap();
            writeln!(
                out,
                "      return Err({{ field: \"{type_name}\", message: \"must be positive\", value }});",
            )
            .unwrap();
            writeln!(out, "    }}").unwrap();
        }
        PredKind::InRange(a, b) => {
            writeln!(out, "    if (!(value >= {a} && value <= {b})) {{").unwrap();
            writeln!(
                out,
                "      return Err({{ field: \"{type_name}\", message: \"must be in range [{a}, {b}]\", value }});",
            )
            .unwrap();
            writeln!(out, "    }}").unwrap();
        }
        PredKind::NonEmpty => {
            writeln!(out, "    if (!(value.length > 0)) {{").unwrap();
            writeln!(
                out,
                "      return Err({{ field: \"{type_name}\", message: \"must be non-empty\", value }});",
            )
            .unwrap();
            writeln!(out, "    }}").unwrap();
        }
        PredKind::MinLength(n) => {
            writeln!(out, "    if (!(value.length >= {n})) {{").unwrap();
            writeln!(
                out,
                "      return Err({{ field: \"{type_name}\", message: \"length must be at least {n}\", value }});",
            )
            .unwrap();
            writeln!(out, "    }}").unwrap();
        }
        PredKind::MaxLength(n) => {
            writeln!(out, "    if (!(value.length <= {n})) {{").unwrap();
            writeln!(
                out,
                "      return Err({{ field: \"{type_name}\", message: \"length must be at most {n}\", value }});",
            )
            .unwrap();
            writeln!(out, "    }}").unwrap();
        }
        PredKind::Length(n) => {
            writeln!(out, "    if (!(value.length === {n})) {{").unwrap();
            writeln!(
                out,
                "      return Err({{ field: \"{type_name}\", message: \"length must be exactly {n}\", value }});",
            )
            .unwrap();
            writeln!(out, "    }}").unwrap();
        }
        PredKind::Matches(pat) => {
            let escaped = escape_ts_string(pat);
            writeln!(
                out,
                "    if (!new RegExp(\"^\" + \"{escaped}\" + \"$\").test(value)) {{",
            )
            .unwrap();
            writeln!(
                out,
                "      return Err({{ field: \"{type_name}\", message: \"must match /{}/\", value }});",
                escape_ts_string(pat),
            )
            .unwrap();
            writeln!(out, "    }}").unwrap();
        }
    }
}

/// Per-function lowering scratchpad. Holds the fresh-temp counter.
struct FnEmit {
    next_tmp: u32,
}

impl FnEmit {
    fn new() -> Self {
        Self { next_tmp: 0 }
    }
    fn fresh(&mut self) -> String {
        let n = self.next_tmp;
        self.next_tmp += 1;
        format!("__r{n}")
    }
}

fn emit_fn(out: &mut String, f: &FnDecl) {
    let params: Vec<String> = f
        .params
        .iter()
        .map(|p| format!("{}: {}", p.name.name, ts_type_ref(&p.type_ref)))
        .collect();
    writeln!(
        out,
        "export function {name}({params}): {ret} {{",
        name = f.name.name,
        params = params.join(", "),
        ret = ts_type_ref(&f.return_type),
    )
    .unwrap();

    let mut emit = FnEmit::new();
    emit_block_as_function_body(out, &f.body, &mut emit, INDENT_STEP);

    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
}

/// Emit a block in tail position — its statements are flattened into the
/// enclosing scope, and the tail expression becomes a `return` statement.
fn emit_block_as_function_body(out: &mut String, block: &Block, emit: &mut FnEmit, indent: usize) {
    for stmt in &block.statements {
        emit_statement(out, stmt, emit, indent);
    }
    let mut stmts = Vec::new();
    let tail = lower_expr(&block.tail, &mut stmts, emit);
    for s in &stmts {
        write_line(out, indent, s);
    }
    write_line(out, indent, &format!("return {tail};"));
}

fn emit_statement(out: &mut String, stmt: &Statement, emit: &mut FnEmit, indent: usize) {
    match stmt {
        Statement::Let(l) => {
            let mut stmts = Vec::new();
            let value = lower_expr(&l.value, &mut stmts, emit);
            for s in &stmts {
                write_line(out, indent, s);
            }
            match &l.type_annot {
                Some(annot) => write_line(
                    out,
                    indent,
                    &format!(
                        "const {name}: {ty} = {value};",
                        name = l.name.name,
                        ty = ts_type_ref(annot),
                    ),
                ),
                None => write_line(
                    out,
                    indent,
                    &format!("const {name} = {value};", name = l.name.name),
                ),
            }
        }
    }
}

fn write_line(out: &mut String, indent: usize, line: &str) {
    for _ in 0..indent {
        out.push(' ');
    }
    out.push_str(line);
    out.push('\n');
}

/// Lower an expression to (statements-needed-before-it, expression-string).
/// The statements list is appended to `stmts`. Expressions that introduce
/// statements (`?`, statement-form `if`/block) push side-effects there and
/// return a simple reference (a temp name) as their expression text.
fn lower_expr(e: &Expr, stmts: &mut Vec<String>, emit: &mut FnEmit) -> String {
    match &e.kind {
        ExprKind::IntLit(n) => n.to_string(),
        ExprKind::StrLit(s) => format!("\"{}\"", escape_ts_string(s)),
        ExprKind::BoolLit(b) => b.to_string(),
        ExprKind::Ident(id) => id.name.clone(),
        ExprKind::Call(name, args) => {
            let args: Vec<String> = args.iter().map(|a| lower_expr(a, stmts, emit)).collect();
            format!("{}({})", name.name, args.join(", "))
        }
        ExprKind::UnaryOp(op, inner) => {
            let inner = lower_expr(inner, stmts, emit);
            let sym = match op {
                UnaryOp::Neg => "-",
                UnaryOp::Not => "!",
            };
            format!("{sym}{inner}")
        }
        ExprKind::BinOp(op, lhs, rhs) => {
            let l = lower_expr(lhs, stmts, emit);
            let r = lower_expr(rhs, stmts, emit);
            if *op == BinOp::Div {
                format!("Math.trunc({l} / {r})")
            } else {
                format!("{l} {} {r}", ts_binop(*op))
            }
        }
        ExprKind::Paren(inner) => {
            let s = lower_expr(inner, stmts, emit);
            format!("({s})")
        }
        ExprKind::Ok(inner) => {
            let s = lower_expr(inner, stmts, emit);
            format!("Ok({s})")
        }
        ExprKind::Err(inner) => {
            let s = lower_expr(inner, stmts, emit);
            format!("Err({s})")
        }
        ExprKind::Question(inner) => {
            // `expr?` introduces a fresh temp, an early-return on Err, and
            // resolves to `__rN.value`.
            let inner_expr = lower_expr(inner, stmts, emit);
            let tmp = emit.fresh();
            stmts.push(format!("const {tmp} = {inner_expr};"));
            stmts.push(format!("if (!{tmp}.ok) return {tmp};"));
            format!("{tmp}.value")
        }
        ExprKind::ConstructorCall {
            type_name,
            method,
            args,
        } => {
            let args: Vec<String> = args.iter().map(|a| lower_expr(a, stmts, emit)).collect();
            format!("{}.{}({})", type_name.name, method.name, args.join(", "))
        }
        ExprKind::If {
            cond,
            then_block,
            else_block,
        } => lower_if(cond, then_block, else_block, stmts, emit),
        ExprKind::Block(b) => lower_block_as_expr(b, stmts, emit),
    }
}

/// Lower an `if`-expression. Uses a ternary if both branches are simple
/// (no statements, no `?` extraction); otherwise emits an IIFE.
fn lower_if(
    cond: &Expr,
    then_block: &Block,
    else_block: &Block,
    stmts: &mut Vec<String>,
    emit: &mut FnEmit,
) -> String {
    let cond_expr = lower_expr(cond, stmts, emit);
    if both_simple(then_block, else_block) {
        // Both branches are single expressions with no extractions — pure
        // expression lowering possible. Use a parenthesised ternary.
        let mut tstmts = Vec::new();
        let testr = lower_expr(&then_block.tail, &mut tstmts, emit);
        debug_assert!(tstmts.is_empty());
        let mut estmts = Vec::new();
        let eestr = lower_expr(&else_block.tail, &mut estmts, emit);
        debug_assert!(estmts.is_empty());
        format!("({cond_expr} ? {testr} : {eestr})")
    } else {
        // Fall back to an IIFE so we can use statements in branches.
        let mut iife = String::new();
        iife.push_str("(() => {\n");
        iife.push_str("    if (");
        iife.push_str(&cond_expr);
        iife.push_str(") {\n");
        let inner_indent = INDENT_STEP * 3;
        emit_block_as_function_body(&mut iife, then_block, emit, inner_indent);
        for _ in 0..(INDENT_STEP * 2) {
            iife.push(' ');
        }
        iife.push_str("} else {\n");
        emit_block_as_function_body(&mut iife, else_block, emit, inner_indent);
        for _ in 0..(INDENT_STEP * 2) {
            iife.push(' ');
        }
        iife.push_str("}\n");
        for _ in 0..(INDENT_STEP) {
            iife.push(' ');
        }
        iife.push_str("})()");
        iife
    }
}

/// A block is "simple" for ternary lowering if it has no statements and its
/// tail expression carries no `?` operators or nested statement-bearing
/// blocks.
fn both_simple(a: &Block, b: &Block) -> bool {
    a.statements.is_empty()
        && b.statements.is_empty()
        && simple_expr(&a.tail)
        && simple_expr(&b.tail)
}

fn simple_expr(e: &Expr) -> bool {
    match &e.kind {
        ExprKind::Question(_) => false,
        ExprKind::Block(b) => b.statements.is_empty() && simple_expr(&b.tail),
        ExprKind::If {
            then_block,
            else_block,
            cond,
        } => simple_expr(cond) && both_simple(then_block, else_block),
        ExprKind::Ok(i) | ExprKind::Err(i) | ExprKind::Paren(i) | ExprKind::UnaryOp(_, i) => {
            simple_expr(i)
        }
        ExprKind::BinOp(_, l, r) => simple_expr(l) && simple_expr(r),
        ExprKind::Call(_, args) | ExprKind::ConstructorCall { args, .. } => {
            args.iter().all(simple_expr)
        }
        _ => true,
    }
}

/// Lower a `Block(...)` used in expression position (not at function/tail).
/// Always emits an IIFE so any inner statements stay confined.
fn lower_block_as_expr(b: &Block, _stmts: &mut Vec<String>, emit: &mut FnEmit) -> String {
    let mut iife = String::new();
    iife.push_str("(() => {\n");
    emit_block_as_function_body(&mut iife, b, emit, INDENT_STEP * 2);
    for _ in 0..INDENT_STEP {
        iife.push(' ');
    }
    iife.push_str("})()");
    iife
}

fn ts_base(b: BaseType) -> &'static str {
    match b {
        BaseType::Int => "number",
        BaseType::String => "string",
        BaseType::Bool => "boolean",
    }
}

fn ts_type_ref(r: &TypeRef) -> String {
    match r {
        TypeRef::Base(b, _) => ts_base(*b).to_string(),
        TypeRef::Named(id) => id.name.clone(),
        TypeRef::Result(t, e, _) => format!("Result<{}, {}>", ts_type_ref(t), ts_type_ref(e)),
        TypeRef::ValidationError(_) => "ValidationError".to_string(),
    }
}

fn ts_binop(op: BinOp) -> &'static str {
    match op {
        BinOp::Or => "||",
        BinOp::And => "&&",
        BinOp::Eq => "===",
        BinOp::NotEq => "!==",
        BinOp::Lt => "<",
        BinOp::LtEq => "<=",
        BinOp::Gt => ">",
        BinOp::GtEq => ">=",
        BinOp::Add => "+",
        BinOp::Sub => "-",
        BinOp::Mul => "*",
        BinOp::Div => "/", // handled separately above
    }
}

fn escape_ts_string(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for c in s.chars() {
        match c {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\t' => out.push_str("\\t"),
            '\r' => out.push_str("\\r"),
            c => out.push(c),
        }
    }
    out
}
